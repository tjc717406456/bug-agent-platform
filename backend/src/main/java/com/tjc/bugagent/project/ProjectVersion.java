package com.tjc.bugagent.project;

/**
 * Imported source version of a project.
 */
public class ProjectVersion {
    private Long id;
    private Long projectId;
    private String sourceType;
    private String branchName;
    private String commitId;
    private String sourcePath;
    private String indexStatus;
    private String indexMessage;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getProjectId() {
        return projectId;
    }

    public void setProjectId(Long projectId) {
        this.projectId = projectId;
    }

    public String getSourceType() {
        return sourceType;
    }

    public void setSourceType(String sourceType) {
        this.sourceType = sourceType;
    }

    public String getBranchName() {
        return branchName;
    }

    public void setBranchName(String branchName) {
        this.branchName = branchName;
    }

    public String getCommitId() {
        return commitId;
    }

    public void setCommitId(String commitId) {
        this.commitId = commitId;
    }

    public String getSourcePath() {
        return sourcePath;
    }

    public void setSourcePath(String sourcePath) {
        this.sourcePath = sourcePath;
    }

    public String getIndexStatus() {
        return indexStatus;
    }

    public void setIndexStatus(String indexStatus) {
        this.indexStatus = indexStatus;
    }

    public String getIndexMessage() {
        return indexMessage;
    }

    public void setIndexMessage(String indexMessage) {
        this.indexMessage = indexMessage;
    }
}

